int	ft_sqrt(int nb)
{
	unsigned int i;

	if (nb < 1)
		return (0);
	i = 1;
	while (i * i != (unsigned)nb)
	{
		if (i * i > (unsigned)nb)
			return (0);
		i++;
	}
	return (i);
}
