int		ft_str_is_alpha(char *str)
{
	int i;
	int count;

	i = 0;
	while (str[i] != '\0')
	{
		count = 0;
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			count++;
		}
		if (!(str[i] >= 'a' && str[i] <= 'z'))
		{
			count++;
		}
		if (count == 2)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
