#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	char	a;
	int		i;

	a = 'z';
	i = 0;
	while (i != 26)
	{
		write(1, &a, 1);
		a = a - 1;
		i++;
	}
}
