#include <unistd.h>

void	check(int *nb)
{
	if (*nb < 0)
	{
		write(1, "-", 1);
		*nb = *nb * -1;
	}
}

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		ft_putnbr(nb / 10);
		write(1, "8", 1);
		return ;
	}
	check(&nb);
	c = nb % 10 + 48;
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	write(1, &c, 1);
}
